#!/usr/bin/env python3
"""Paste benchmark/results/summary.md + spatial.md into the README between
their respective BEGIN/END markers.

Intended to be invoked from CI or `make benchmark-update` after
`benchmark/run.sh` (token benchmark) and `benchmark/spatial.py`
(spatial benchmark) have been run.
"""
from __future__ import annotations

import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SUMMARY = ROOT / "benchmark" / "results" / "summary.json"
SPATIAL = ROOT / "benchmark" / "results" / "spatial.json"
README = ROOT / "README.md"

BEGIN = "<!-- BENCHMARK:BEGIN -->"
END = "<!-- BENCHMARK:END -->"
SPATIAL_BEGIN = "<!-- SPATIAL:BEGIN -->"
SPATIAL_END = "<!-- SPATIAL:END -->"


def fmt_pct(x: float) -> str:
    return f"{x * 100:.1f}%"


def render(rows: list[dict]) -> str:
    # Per-fixture table — bold whichever engine has the winning F1 on that row.
    lines: list[str] = []
    lines.append("<!-- generated by benchmark/run.sh — do not edit by hand -->")
    lines.append("")
    lines.append(
        "| fixture | engine | wall-clock | tokens | recall | precision | F1 |"
    )
    lines.append("|---|---|---:|---:|---:|---:|---:|")

    for row in rows:
        engines: list[tuple[str, dict]] = [("spdf", row["spdf"])]
        if "lite" in row:
            engines.append(("liteparse", row["lite"]))
        winner = max(engines, key=lambda kv: kv[1]["f1"])[0]
        for name, e in engines:
            cells = [
                f"{e['ms']} ms",
                str(e["tokens"]),
                fmt_pct(e["recall"]),
                fmt_pct(e["precision"]),
                fmt_pct(e["f1"]),
            ]
            if name == winner:
                bold_name = f"**{name}**"
                cells = [f"**{c}**" for c in cells]
            else:
                bold_name = name
            lines.append(
                f"| {row['fixture']} | {bold_name} | {' | '.join(cells)} |"
            )

    # Summary line with the mean F1 / wall-clock.
    def mean(key: str, engine: str) -> float:
        vals = [r[engine][key] for r in rows if engine in r]
        return sum(vals) / len(vals) if vals else 0.0

    lines.append("")
    mean_spdf_f1 = mean("f1", "spdf")
    mean_spdf_ms = mean("ms", "spdf")
    summary_parts = [
        f"**Mean over fixtures:** spdf **F1 {fmt_pct(mean_spdf_f1)}** in "
        f"**{mean_spdf_ms:.0f} ms**"
    ]
    if any("lite" in r for r in rows):
        mean_lite_f1 = mean("f1", "lite")
        mean_lite_ms = mean("ms", "lite")
        summary_parts.append(
            f"liteparse F1 {fmt_pct(mean_lite_f1)} in {mean_lite_ms:.0f} ms"
        )
    lines.append("; ".join(summary_parts) + ".")
    lines.append("")
    return "\n".join(lines)


def render_spatial(rows: list[dict]) -> str:
    # Per-fixture spatial table. Winner per row = highest mean_iou.
    lines: list[str] = []
    lines.append("<!-- generated by benchmark/spatial.py — do not edit by hand -->")
    lines.append("")
    lines.append(
        "| fixture | engine | matched | mean IoU | IoU≥0.5 | centroid err |"
    )
    lines.append("|---|---|---:|---:|---:|---:|")

    for row in rows:
        engines: list[tuple[str, dict]] = [("spdf", row["spdf"])]
        if "lite" in row:
            engines.append(("liteparse", row["lite"]))
        winner = max(engines, key=lambda kv: kv[1]["mean_iou"])[0]
        for name, s in engines:
            cells = [
                str(s["matches"]),
                f"{s['mean_iou']:.3f}",
                fmt_pct(s["iou_ge_threshold_rate"]),
                f"{s['mean_centroid_err_pt']:.2f} pt",
            ]
            if name == winner:
                bold_name = f"**{name}**"
                cells = [f"**{c}**" for c in cells]
            else:
                bold_name = name
            lines.append(
                f"| {row['fixture']} | {bold_name} | {' | '.join(cells)} |"
            )

    def mean(key: str, engine: str) -> float:
        vals = [r[engine][key] for r in rows if engine in r]
        return sum(vals) / len(vals) if vals else 0.0

    lines.append("")
    summary_parts = [
        f"**Mean over fixtures:** spdf **mean IoU {mean('mean_iou','spdf'):.3f}**, "
        f"**{fmt_pct(mean('iou_ge_threshold_rate','spdf'))}** of matches ≥ 0.5, "
        f"centroid error **{mean('mean_centroid_err_pt','spdf'):.2f} pt**"
    ]
    if any("lite" in r for r in rows):
        summary_parts.append(
            f"liteparse {mean('mean_iou','lite'):.3f} / "
            f"{fmt_pct(mean('iou_ge_threshold_rate','lite'))} / "
            f"{mean('mean_centroid_err_pt','lite'):.2f} pt"
        )
    lines.append("; ".join(summary_parts) + ".")
    lines.append("")
    return "\n".join(lines)


def splice(readme: str, begin: str, end: str, payload: str) -> str:
    pattern = re.compile(re.escape(begin) + r".*?" + re.escape(end), flags=re.DOTALL)
    if not pattern.search(readme):
        print(f"{begin} / {end} markers missing from {README}", file=sys.stderr)
        return readme
    return pattern.sub(f"{begin}\n{payload}\n{end}", readme)


def main() -> int:
    if not SUMMARY.is_file():
        print(f"{SUMMARY} missing; run benchmark/run.sh first", file=sys.stderr)
        return 1
    readme = README.read_text()

    token_data = json.loads(SUMMARY.read_text())
    new = splice(readme, BEGIN, END, render(token_data["rows"]))

    if SPATIAL.is_file():
        spatial_data = json.loads(SPATIAL.read_text())
        new = splice(new, SPATIAL_BEGIN, SPATIAL_END, render_spatial(spatial_data["rows"]))
    else:
        print(
            f"note: {SPATIAL} missing — skipping spatial block "
            "(run benchmark/spatial.py to generate)",
            file=sys.stderr,
        )

    if new == readme:
        print("README already up to date.")
        return 0
    README.write_text(new)
    print(f"updated README {README}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
